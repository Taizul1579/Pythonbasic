mark = int(input("Enter Your Mark : "))
if mark >= 80:
    print("Congratulation!! Your Result Is :A+")
elif mark >= 70:
    print("Congratulation!! Your Result Is :A")
elif mark >= 60:
    print("Congratulation!! Your Result Is :A-")
elif mark >= 50:
    print("Congratulation!! Your Result Is :B")
elif mark >= 40:
    print("Congratulation!! Your Result Is :C")
elif mark >= 33:
    print("Congratulation!! Your Result Is :D")
else:
    print("Sorry!! \nYou are Fail")
